public class TrackClass{
    int CID;
    int ID;
    int year;
    int SET;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getSET() {
        return SET;
    }

    public void setSET(int SET) {
        this.SET = SET;
    }

    public int getCID() {
        return CID;
    }

    public void setCID(int CID) {
        this.CID = CID;
    }
}
